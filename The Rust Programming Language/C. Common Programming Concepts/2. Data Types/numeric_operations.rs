// Numeris Operations

fn main() {
    //addition
    let sum = 5 + 10;
    println!("sum: {sum}");

    //subraction
    let diff = 95.5 - 4.3;
    println!("difference: {diff}");

    //multiplication
    let prod = 4 * 30;
    println!("product: {prod}");

    //division
    let quot = 56.7 / 32.2;
    let truncated = -5 / 3; // results in -1
    println!("quotient: {quot}");
    println!("truncated part: {truncated}");

    //remainder
    let remd = 43 % 5;
    println!("remainder: {remd}");
}
